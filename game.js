'use strict';

//////////////// Loop Switch JS - ES6 ////////////
var idade = 17;

switch (idade) {
	case 17:
		document.write('Tem 17' + '<br>');
		break;
	case 18:
		document.write('Tem 18' + '<br>');
		break;
	default:
		document.write('ERROR' + '<br>');
		break;
}
